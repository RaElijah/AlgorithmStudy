/*
아래 예제와 같이 새싹을 출력하시오.

         ,r'"7
r`-_   ,'  ,/
 \. ". L_r'
   `~\/
      |
      |
*/
public class FirstAl {
    public static void main(String[] args) {
        // escape sequence \을 이용하여 \\, \" 을 쓴다.
        System.out.println("         ,r'\"7");
        System.out.println("r`-_   ,'  ,/");
        System.out.println(" \\. \". L_r'");
        System.out.println("   `~\\/");
        System.out.println("      |");
        System.out.println("      |");
    }
}
